package com.smartelligynt.api;

public class Events {
	
	private String en;
	private String ev;
	private long et;
	
	public String getEn() {
		return en;
	}
	public void setEn(String en) {
		this.en = en;
	}
	public String getEv() {
		return ev;
	}
	public void setEv(String ev) {
		this.ev = ev;
	}
	public long getEt() {
		return et;
	}
	public void setEt(long et) {
		this.et = et;
	}

}
